
import java.util.HashSet;
import java.util.Set;

public class HashSetExample{
    public static void main(String[] args) {
        
        Set<String> set = new HashSet<>();
        
        set.add("Vaibhav");
        set.add("Shreya");
        set.add("Atharv");
        set.add("Prasad");
        set.add("Yash");
        set.add("Parth");
        set.add("Prajwal");

        System.out.println(set);
    }
}